#abs函数：用来返回数字绝对值，如：
print(abs(-10))
#如果是虚数，则返回他的模
print(abs(3+4j))
#pow函数：用来返回x的y次幂，也可以加参数pow（x，y[，z]），z的作用是x的y次方对z求余数
print(pow(2,4))
print(pow(2,4,5))
#round函数：用于对浮点数进行四舍五入操作，第二个参数告诉round函数精确到小数点后的位数
print(round(3.14159265354))
print(round(3.14159265354,3))
#divmod函数：把除法和取余运算结合起来，返回一个包含商和余数的元组。对整数来说，它就是x/y取整和取余的结果
print(divmod(8,5))